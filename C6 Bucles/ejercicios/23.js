function doWhile(num) {
  // Aumenta el valor de "num" recibido en 5 hasta un límite de 8 veces.
  // Retorna el valor final.
  // PISTA: Utiliza el bucle do-while.
  // Tu código:

  var resultado = num


  for (let i = 0; i < 8; i++) {
    resultado +=  5
    
  }

  return resultado
}

module.exports = doWhile;